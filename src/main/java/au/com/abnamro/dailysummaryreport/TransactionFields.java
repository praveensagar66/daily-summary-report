package au.com.abnamro.dailysummaryreport;

public class TransactionFields {

	private String clientType;
	private String clientNumber;
	private String accountNumber;
	private String subAccountNumber;
	private String exchangeCode;
	private String productGroupCode;
	private String symbol;
	private String expirationCode;
	private Double quantityLong;
	private Double quantityShort;
	public String getClientType() {
		return clientType;
	}
	public void setClientType(String clientType) {
		this.clientType = clientType;
	}
	public String getClientNumber() {
		return clientNumber;
	}
	public void setClientNumber(String clientNumber) {
		this.clientNumber = clientNumber;
	}
	public String getAccountNumber() {
		return accountNumber;
	}
	public void setAccountNumber(String accountNumber) {
		this.accountNumber = accountNumber;
	}
	public String getSubAccountNumber() {
		return subAccountNumber;
	}
	public void setSubAccountNumber(String subAccountNumber) {
		this.subAccountNumber = subAccountNumber;
	}
	public String getExchangeCode() {
		return exchangeCode;
	}
	public void setExchangeCode(String exchangeCode) {
		this.exchangeCode = exchangeCode;
	}
	public String getProductGroupCode() {
		return productGroupCode;
	}
	public void setProductGroupCode(String productGroupCode) {
		this.productGroupCode = productGroupCode;
	}
	public String getSymbol() {
		return symbol;
	}
	public void setSymbol(String symbol) {
		this.symbol = symbol;
	}
	public String getExpirationCode() {
		return expirationCode;
	}
	public void setExpirationCode(String expirationCode) {
		this.expirationCode = expirationCode;
	}
	public Double getQuantityLong() {
		return quantityLong;
	}
	public void setQuantityLong(Double quantityLong) {
		this.quantityLong = quantityLong;
	}
	public Double getQuantityShort() {
		return quantityShort;
	}
	public void setQuantityShort(Double quantityShort) {
		this.quantityShort = quantityShort;
	}
	
		
}
